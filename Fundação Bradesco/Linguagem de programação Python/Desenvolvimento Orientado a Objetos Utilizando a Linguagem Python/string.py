# FUNÇÕES PARA STRING:
# Função len(): O tamanho de uma string pode ser obtido utilizando a função len(). 
# Essa função retorna o número de caracteres armazenados em uma string. Ela retorna 
# um único valor, indicando a quantidade de caracteres contidos na variável.

class Main:
    nome = "Ana"
    print(len(nome))